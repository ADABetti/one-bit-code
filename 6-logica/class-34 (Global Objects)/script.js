// Objetos globais são próprios do JS. Começam com letra maiúscula.
// Alguns exemplos são: Array, Object, String, Number, Date, Math...

let lista = Array("A", "B", "C");
console.log(lista);

let nome = Object("Lucas");
console.log(nome);

console.log(Number.isInteger(4.5));
console.log(Number.isInteger(4));

console.log(Math.ceil(4.5));
console.log(Math.floor(4.5));
